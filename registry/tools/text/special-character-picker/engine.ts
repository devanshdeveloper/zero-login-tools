export function getAllChars(): string[] {
  return specialCharGroups.flatMap((g) => g.chars);
}

export function getCharUnicode(char: string): string {
  if (!char.length) return "";
  return char
    .split("")
    .map((c: any) => "U+" + c.charCodeAt(0).toString(16).toUpperCase().padStart(4, "0"))
    .join(" ");
}
